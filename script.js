let number = parseInt(prompt("Enter a number"));

if (number == 0) {
  console.log("number is zero:" + number);
} else if (number > 0) {
  console.log("number is Positive:" + number);
} else if (number < 0) {
  console.log("number is negative:" + number);
} else {
  console.log("Enter a Valid number!!");
}
